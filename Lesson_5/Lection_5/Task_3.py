"""
Распаковка с упаковкой(упаковка - это символ *,он упаковывает свободные переменные)
"""


data = [1,2,3,4,5,6,7]

a,b,c,*d = data
print(f'{a=} {b=} {c=} {d=}')
#a=1 b=2 c=3 d=[4, 5, 6, 7]
a,b,*c,d = data
print(f'{a=} {b=} {c=} {d=}')
#a=1 b=2 c=[3, 4, 5, 6] d=7
a,*b,c,d = data
print(f'{a=} {b=} {c=} {d=}')
#a=1 b=[2, 3, 4, 5] c=6 d=7
*a,b,c,d = data
print(f'{a=} {b=} {c=} {d=}')
#a=[1, 2, 3, 4] b=5 c=6 d=7
